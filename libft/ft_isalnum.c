#include "libft.h"

int	ft_isalnum(int nb)
{
	if ((nb >= '0' && nb <= '9') || (nb >= 'A' && nb <= 'Z')
		|| (nb >= 'a' && nb <= 'z'))
		return (1);
	return (0);
}
