#include "libft.h"

int    ft_strcmp(char *s1, char *s2)
{
	int     i;
	i = 0;
	
	if (!s1)
		return (0);
	while (s1[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	if (s2[i])
		return (-s2[i]);
	return (0);
}