#include "libft.h"

char	*ft_strrchr(const char *string, int c)
{
	char	*s;
	int		i;

	i = 0;
	s = (char *)string;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
